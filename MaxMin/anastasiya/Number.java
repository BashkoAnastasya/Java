package com.intexsoft.bashko.anastasiya;

public class Number {
	
	private int number;
	private int length;
	
	public Number(int number, int length) {
		this.number=number;
		this.length=length;
	}
	
	public int  getLength() {
		return length;
	}
	public int getnumber() {
		return number;
	}
	
	

}
